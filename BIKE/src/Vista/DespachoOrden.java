    public void LimpiarPantalla(){
            txtdoc.setText("");
            cmbtipdoc.setSelectedIndex(0);
            txtname.setText("");
        try{
            for( int i=0; i < tbl_despacho.getRowCount(); i++ ){
                dtm.removeRow(i);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error: "+e.getLocalizedMessage(), title, 0);
        }
    }
    
    
