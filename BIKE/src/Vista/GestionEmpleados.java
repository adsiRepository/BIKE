    public final void ExtractItemsExt(){
        MdlData ext = new MdlData();
        CtrData itemsx = new CtrData("proveedores");
        ext.ViewForKey(itemsx.ExtractAll(), "codprov");
        if( ext.getResults().size() > 0 ){
            for(int i=0; i<ext.getResults().size(); i++){
                cmbcodprov.addItem(""+ext.getResults().toArray()[i]);
            } 
        }
    }
    
    public void LimpiarPantalla(){
        cmbcodprov.setSelectedIndex(0);
        cmbcodprod.setSelectedIndex(0);
        txtfchin.setText(null);
        txthrin.setText(null);
        txtcant.setText(null);
        
        try{
            for( int i=0; i<tblmerc.getRowCount(); i++ ){
                dtm.removeRow(i);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error: "+e.getMessage(),"Borrar Mercancia",0);
        }
    }
    
    public void miBusca(){
        CtrData data = new CtrData(mitabla);
        MdlData exe = new MdlData();
        
        try{
            String camp = "codpro";
            
            exe.Search( data.ExtWhere( camp, ""+obs) );
            
            if(!exe.getResults().isEmpty()){
                dtm.addRow(exe.getResults().toArray());
            }
            txtfchin.setText(""+exe.getResults().toArray()[4]);
            txthrin.setText(""+exe.getResults().toArray()[5]);
            txtcant.setText(""+exe.getResults().toArray()[3]);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Error: \n"+ex.getLocalizedMessage(), "GMercancia Buscar",0);
        }
        
        data = null;
        exe = null;
    }
    
    
    private void Ingresar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Ingresar_ActionPerformed
        // TODO add your handling code here:
        
        CtrData data = new CtrData(mitabla);
        ArrayList<Object> newreg = new ArrayList<>();
        Fechas fecha = new Fechas();
        MdlData exe = new MdlData();//
        
        String codprov, codprod, costo, venta;
        int cant = 0;
        
        try{
            codprov = String.valueOf(cmbcodprov.getSelectedItem());
            codprod = String.valueOf(cmbcodprod.getSelectedItem());
            
            exe.ViewForKey(data.ExtWhere("codpro", codprod), "inv");
            
            if( !exe.getResults().isEmpty() ){
                cant = Integer.parseInt(""+exe.getResults().get(0));
            }
            
            newreg.add(cant+Integer.parseInt(txtcant.getText()));
            newreg.add(fecha.getDate());
            newreg.add(fecha.getHour());
            
            String[] campos = {"inv","fechasurtido","horasurtido"};
            
            exe.Update(data.ChangeReg("codpro", codprod, newreg, campos ));
            
            LimpiarPantalla();
            
            txtfchin.setText(fecha.getDate());
            txthrin.setText(fecha.getHour());
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Error :"+ex.getMessage(), "GMercancía Guardar", 0);
        }
        
        data = null;
        exe = null;
        
    }//GEN-LAST:event_Ingresar_ActionPerformed

