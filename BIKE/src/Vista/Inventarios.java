    private void Modificar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Modificar_ActionPerformed
        // TODO add your handling code here:
        
        CtrData data = new CtrData(mitabla);
        MdlData exe = new MdlData();
        
        String camps[] = {"codprov","idprov","idtipo","nombre","direccion","telefono","mail"};
        
        ArrayList<Object> newdata = new ArrayList<>();
        
        try{
            newdata.add(txtcod.getText());
            newdata.add(txtid.getText());
            newdata.add(cmbtid.getSelectedItem());
            newdata.add(txtnm.getText());
            newdata.add(txtdir.getText());
            newdata.add(txttl.getText());
            newdata.add(txtmil.getText());

            exe.Update(data.ChangeReg( "codprov", ""+obs, newdata, camps ));
            
            LimpiarPantalla();
            
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Error: \n"+ex.getLocalizedMessage(), "GUsers Modificar", 0);
        }
        
        exe = null;
        data = null;
        
    }//GEN-LAST:event_Modificar_ActionPerformed

