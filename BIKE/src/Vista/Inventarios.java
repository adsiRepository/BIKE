    public void LimpiarPantalla(){
        txtcod.setText("");
        txtid.setText("");
        cmbtid.setSelectedIndex(0);
        txtnm.setText("");
        txtdir.setText("");
        txttl.setText("");
        txtmil.setText("");
        try{
            for( int i=0; i<tblprov.getRowCount(); i++ ){
                dtm.removeRow(i);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error: "+e.getLocalizedMessage(), title, 0);
        }
    }
    
    public void miBusca(){
        CtrData data = new CtrData(mitabla);
        MdlData exe = new MdlData();
        
        try{
            String camp = "codprov";
            
            exe.Search(data.ExtWhere( camp, ""+obs ) );
            
            if(!exe.getResults().isEmpty()){
                dtm.addRow(exe.getResults().toArray());
            }
            
            txtcod.setText(""+exe.getResults().toArray()[0]);
            txtid.setText(""+exe.getResults().toArray()[1]);
            cmbtid.setSelectedItem(""+exe.getResults().toArray()[2]);
            txtnm.setText(""+exe.getResults().toArray()[3]);
            txtdir.setText(""+exe.getResults().toArray()[4]);
            txttl.setText(""+exe.getResults().toArray()[5]);
            txtmil.setText(""+exe.getResults().toArray()[6]);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Error: \n"+ex.getLocalizedMessage(), title,0);
        }
        exe = null;
        data = null;
    }
    
    
    private void Añadir_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Añadir_ActionPerformed
        // TODO add your handling code here:
        
        MdlData exe = new MdlData();
        CtrData data = new CtrData(mitabla);
        
        String codprov;
        
        ArrayList<Object> newreg = new ArrayList<>();
        
        if( "".equals(txtcod.getText()) ){
            codprov = txtid.getText();
        }
        else{
            codprov = txtcod.getText();
        }
        
        newreg.add(codprov);
        newreg.add(txtid.getText());
        newreg.add(cmbtid.getSelectedItem());
        newreg.add(txtnm.getText());
        newreg.add(txtdir.getText());
        newreg.add(txttl.getText());
        newreg.add(txtmil.getText());
        
        exe.SaveToBD(data.ArrayToBD(newreg));
        
        LimpiarPantalla();
        
        exe = null;
        data = null;
        
    }//GEN-LAST:event_Añadir_ActionPerformed

